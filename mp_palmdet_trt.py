import numpy as np
import cv2 as cv
import tensorrt as trt
import pycuda.driver as cuda
import pycuda.autoinit
import warnings

warnings.filterwarnings("ignore", category=DeprecationWarning, module="tensorrt")
warnings.filterwarnings("ignore", category=UserWarning, module="tensorrt")

class MPPalmDetTRT:
    def __init__(self, modelPath, nmsThreshold=0.3, scoreThreshold=0.5, topK=5000, use_gpu=True):
        self.model_path = modelPath
        self.nms_threshold = nmsThreshold
        self.score_threshold = scoreThreshold
        self.topK = topK
        self.use_gpu = use_gpu

        self.input_size = np.array([192, 192]) # wh

        # 加载 TensorRT 引擎
        self.logger = trt.Logger(trt.Logger.WARNING)
        with open(modelPath, 'rb') as f:
            self.engine = trt.Runtime(self.logger).deserialize_cuda_engine(f.read())
        self.context = self.engine.create_execution_context()

        # 获取输出名称和顺序
        self.output_names = []
        self.output_shapes = {}
        for i in range(1, self.engine.num_bindings):
            name = self.engine.get_binding_name(i)
            self.output_names.append(name)
            self.output_shapes[name] = self.engine.get_binding_shape(i)
        
        # 打印输出信息用于调试
        print(f"TensorRT引擎输出顺序:")
        for i, name in enumerate(self.output_names):
            print(f"  {i}: {name}, 形状={self.output_shapes[name]}")

        # 分配内存
        self.inputs = []
        self.outputs = []
        self.bindings = []
        self.stream = cuda.Stream()

        for binding in self.engine:
            size = trt.volume(self.engine.get_binding_shape(binding))
            dtype = trt.nptype(self.engine.get_binding_dtype(binding))
            host_mem = cuda.pagelocked_empty(size, dtype)
            device_mem = cuda.mem_alloc(host_mem.nbytes)
            self.bindings.append(int(device_mem))
            if self.engine.binding_is_input(binding):
                self.inputs.append(host_mem)
                self.inputs.append(device_mem)
            else:
                self.outputs.append(host_mem)
                self.outputs.append(device_mem)

        self.anchors = self._load_anchors()

    def _preprocess(self, image):
        """预处理图像"""
        pad_bias = np.array([0., 0.]) # left, top
        ratio = min(self.input_size / image.shape[:2])
        if image.shape[0] != self.input_size[0] or image.shape[1] != self.input_size[1]:
            # keep aspect ratio when resize
            ratio_size = (np.array(image.shape[:2]) * ratio).astype(np.int32)
            image = cv.resize(image, (ratio_size[1], ratio_size[0]))
            pad_h = self.input_size[0] - ratio_size[0]
            pad_w = self.input_size[1] - ratio_size[1]
            pad_bias[0] = left = pad_w // 2
            pad_bias[1] = top = pad_h // 2
            right = pad_w - left
            bottom = pad_h - top
            image = cv.copyMakeBorder(image, top, bottom, left, right, cv.BORDER_CONSTANT, None, (0, 0, 0))
        image = cv.cvtColor(image, cv.COLOR_BGR2RGB)
        image = image.astype(np.float32) / 255.0 # norm
        pad_bias = (pad_bias / ratio).astype(np.int32)
        # NCHW格式: hwc -> nchw
        blob = np.transpose(image, (2, 0, 1))
        return blob[np.newaxis, :, :, :], pad_bias

    def _postprocess(self, output_dict, img_size, pad_bias):
        """后处理输出"""
        # ONNX输出顺序:
        #   Identity: [1, 2016, 18] - 包含 box_delta (4) + landmark_delta (14)
        #   Identity_1: [1, 2016, 1] - score
        
        # TensorRT输出顺序:
        #   Identity_1: [1, 2016, 1] - score
        #   Identity: [1, 2016, 18] - 包含 box_delta (4) + landmark_delta (14)
        
        # 按名称获取输出
        combined = output_dict['Identity'].squeeze()  # [2016, 18]
        score = output_dict['Identity_1'].squeeze()   # [2016]
        
        # 分离box_delta和landmark_delta
        box_delta = combined[:, :4]    # [2016, 4]
        landmark_delta = combined[:, 4:]  # [2016, 14]

        scale = max(img_size)

        # 计算边界框
        cxy_delta = box_delta[:, :2] / self.input_size
        wh_delta = box_delta[:, 2:] / self.input_size
        xy1 = (cxy_delta - wh_delta / 2 + self.anchors) * scale
        xy2 = (cxy_delta + wh_delta / 2 + self.anchors) * scale
        boxes = np.concatenate([xy1, xy2], axis=1)

        # 计算置信度
        scores = 1 / (1 + np.exp(-score))

        # 过滤低置信度检测
        mask = scores > self.score_threshold
        boxes = boxes[mask]
        scores = scores[mask]
        landmark_delta = landmark_delta[mask]

        if len(boxes) == 0:
            return []

        # NMS
        indices = cv.dnn.NMSBoxes(boxes.tolist(), scores.tolist(), self.score_threshold, self.nms_threshold)
        if len(indices) == 0:
            return []

        indices = indices.flatten() if isinstance(indices, np.ndarray) else [i[0] for i in indices]
        boxes = boxes[indices]
        scores = scores[indices]
        landmark_delta = landmark_delta[indices]

        # 调整边界框
        boxes[:, [0, 2]] -= pad_bias[0]
        boxes[:, [1, 3]] -= pad_bias[1]

        # 调整关键点
        landmark_pred = landmark_delta.reshape(-1, 7, 2)
        landmark_pred = landmark_pred / self.input_size
        selected_anchors = self.anchors[indices]
        for idx, landmark in enumerate(landmark_pred):
            landmark += selected_anchors[idx]
        landmark_pred *= scale
        landmark_pred -= pad_bias

        # 返回与ONNX版本相同的格式: [bbox_coords, landmarks_coords, score]
        results = []
        for i in range(len(boxes)):
            result = np.r_[boxes[i], landmark_pred[i].flatten(), scores[i]]
            results.append(result)

        return results

    def infer(self, image):
        """执行推理"""
        h, w, _ = image.shape

        # Preprocess
        input_blob, pad_bias = self._preprocess(image)

        # Forward
        np.copyto(self.inputs[0], input_blob.ravel())
        cuda.memcpy_htod_async(self.inputs[1], self.inputs[0], self.stream)
        self.context.execute_async_v2(bindings=self.bindings, stream_handle=self.stream.handle)
        
        # 复制所有输出数据
        output_count = 0
        for i in range(self.engine.num_bindings):
            if not self.engine.binding_is_input(i):
                host_idx = output_count * 2
                device_idx = output_count * 2 + 1
                cuda.memcpy_dtoh_async(self.outputs[host_idx], self.outputs[device_idx], self.stream)
                output_count += 1
        
        self.stream.synchronize()

        # 按名称解析输出
        output_dict = {}
        output_count = 0
        for i in range(self.engine.num_bindings):
            if not self.engine.binding_is_input(i):
                name = self.output_names[output_count]
                output_shape = self.output_shapes[name]
                output_size = trt.volume(output_shape)
                output_index = output_count * 2
                output_data = self.outputs[output_index][:output_size].reshape(output_shape)
                output_dict[name] = output_data
                output_count += 1

        # Postprocess
        results = self._postprocess(output_dict, np.array([w, h]), pad_bias)

        return results

